class Usuario:
    def __init__(self, id, nombre, email, contraseña, numero_de_telefono, foto_del_perfil):
        self.id = id
        self.nombre = nombre
        self.email = email
        self.contraseña = contraseña
        self.numero_de_telefono = numero_de_telefono
        self.foto_del_perfil = foto_del_perfil
